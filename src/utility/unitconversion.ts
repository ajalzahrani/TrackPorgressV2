export function cvtMeterToCm(m: number) {
  return m * 100;
}

export function cvtCmtoMeter(m: number) {
  return m / 100;
}

export function cvtMmToCm(mm: number) {
  return mm / 10;
}

export function cvtMeterToInch(m: number) {
  return m * 39.37;
}

export function cvtInchToMeter(inc: number) {
  return inc * 0.0254;
}

export function cvtKgToLb(kg: number) {
  return kg / 2.205;
}

export function cvtLbToKg(lb: number) {
  return lb * 2.205;
}
